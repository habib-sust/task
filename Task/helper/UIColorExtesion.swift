extension UIColor {
    public class var customYellow: UIColor {
        return  UIColor(red: 253/255, green: 190/255, blue: 17/255, alpha: 1)
    }
    public class var customBlue: UIColor {
        return UIColor(red: 53/255, green: 121/255, blue: 204/255, alpha: 1)
    }
}
